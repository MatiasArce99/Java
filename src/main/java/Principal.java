public class Principal {

    public static void main(String[] args){
        int numero = 6;
        System.out.println("Número entero: "+numero);
        double decimal = 7.5;
        System.out.println("Número flotante: "+decimal);
        byte edad = 35;
        System.out.println("Entero corto (byte): "+edad);
        char letra = 'a';
        System.out.println("Caracter: "+letra);
        boolean booleano = true;
        System.out.println("Valor booleano: "+booleano);
    }

}